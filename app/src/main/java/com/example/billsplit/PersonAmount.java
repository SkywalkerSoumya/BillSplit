package com.example.billsplit;

public class PersonAmount {

    private String pName;
    private int pAmount;

    public PersonAmount(String pName, int pAmount) {
        this.pName = pName;
        this.pAmount = pAmount;
    }

    public String getpName() {
        return pName;
    }


    public int getpAmount() {
        return pAmount;
    }

}
